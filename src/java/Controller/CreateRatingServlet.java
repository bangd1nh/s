    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            // Lấy giá trị rating từ request
            int userID = Integer.parseInt(request.getParameter("userID"));
            int listingID = Integer.parseInt(request.getParameter("listingID"));
            int rating = Integer.parseInt(request.getParameter("rating"));

            // Lấy thời điểm hiện tại
            Timestamp ratedAt = new Timestamp(System.currentTimeMillis());

            // Gọi hàm insertRating từ DAO để lưu vào cơ sở dữ liệu
            if (DAO.RatingDAL.insertRating(userID, listingID, rating, ratedAt)) {
                response.getWriter().write("Rating successfully inserted");
            } else {
                response.getWriter().write("Failed to insert rating");
            }
        } catch (NumberFormatException e) {
            response.getWriter().write("Invalid input for rating");
        }

    }

